   11. АДМИНИСТРАТИВНЫЕ НАКАЗАНИЯ<br>
<br>
 11.1. Если организаторы не получат прогноз от игрока основного состава, вместо отсутствующего проставляется прогноз, сгенерированный программой-генератором случайных чисел.<br>
Исходы, угаданные "генератором", не учитываются в личный зачёт.<br>
Список сгенерированных прогнозов должен быть опубликован до начала первого МдП тура.<br>
Прогнозы-генераторы заменят отсутствующие прогнозы в опубликованном порядке.<br>
Если какие-нибудь из этих прогнозов сервером будут получены с опозданием, например прогнозы, своевременно отправленные на e-mail вице-президента, то они заменят сгенерированные прогнозы.<br>
Если по какой-либо причине прогноз одного из игроков впоследствии должен быть заменен на генератор, то он заменяется:<br>
- в случае если ему при публикации прогнозов ранее ставился в соответствие прогноз генератора - возвращается этот прогноз генератора;<br>
- в противном случае берется последний из списка генераторов прогноз (если таких игроков 2 - 2 последних в обратном порядке и т.д.) (4.9)<br>
<br>
 11.2. Игрок основного состава, не приславший прогноз, получает предупреждение (желтую карточку).<br>
За каждую вторую желтую карточку игрок получает красную карточку.<br>
За каждую желтую карточку в матче игрок получает 1 штрафное очко, за каждую красную - 3 штрафных очка. (13.8)<br>
Ставки проштрафившегося игрока в количестве штрафных очков заменяются на 0, начиная с со ставки на победу хозяев в первом МдП.<br>
Если первый МдП, не может быть учтён, штраф переносится на второй МдП, а если не учитывается первый тайм, то на первый МдП второго тайма и т.д.<br>
За прогноз в неверном формате (повторное использование цифры в одном тайме или при неуказанном варианте исхода) игрок получает предупреждение.<br>
При этом на 0 заменяется не первая ставка, а все его ставки на неправильно оформленные исходы.<br>
<br>
 11.3. Если на матч является менее 3 игроков сборной, она получает техническое поражение 0:3 и штрафуется -1 очком в турнирной таблице.<br>
<br>
 11.4. Президент ФП ФИФА имеет право оштрафовать команду тренера, допустившего бестактное поведение.<br>
Под это понятие попадает неуважительное отношение или оскорбление собеседника, других игроков и организаторов игры, а также самой игры ФутболПрогноз и ее правил.<br>
Дисквалификация состоит в лишении команды от 3 до 5 очков в турнире и подлежит беспрекословному выполнению руководством Лиги Наций и обжалованию не подлежит (11.1)<br>
<br>
 11.5. За подачу прогноза от имени другой команды без согласия ее тренера, команда нарушителя "награждается" красной карточкой.<br>
Эффект карточки будет применён к первому игроку основного состава в туре, в котором произошло нарушение.<br>
Дисквалификация за нарушение в Лиге Наций будет рассмотрена на Совете ПФЛ, в Лиге Сайтов - на Совете Мастер-серии.<br>
За умышленное изменение даты и времени отправки письма с прогнозом или иную фальсификацию игрок получает 3 красные карточки (т.е. все его ставки на первый тайм заменяются нулями) в этом и трех ближайших турах с его участим в основном составе.<br>
<br>
   12. Протесты и спорные ситуации<br>
<br>
 12.1. Игрок вправе подать протест по поводу своего прогноза или прогноза любого другого игрока не позднее 5 дней после публикации итогов тура.<br>
После этого срока протесты не принимаются.<br>
Протест подается президенту Лиги Наций и может быть продублирован вице-президенту.<br>
В случае не удовлетворения протеста может быть подан протест непосредственно в ФП ФИФА.<br>
Игрок также вправе подать протест на любое другое решение (например встречный протест) на тех же условиях - не позднее 5 дней после публикации этого решения.<br>
Если игрок сообщил предварительно о том что будет отсутствовать длительный срок, то он имеет право подать протест на событие, произошедшее во время этого срока, не позднее месяца после события.<br>
Игрок вправе подать протест на некорректное оформление программки тура.<br>
Если таких протестов будет не менее трех и они будут удовлетворены президентом Лиги Наций, ФП УЕФА или ФП ФИФА, то тур должен быть переигран (12.5)<br>
<br>
 12.2. Если итоги тура были посчитаны неправильно, то игрок имеет право подать протест по прошествии любого времени до конца турнира (12.6)<br>
<br>
 12.3. В случае возникновения спорной ситуации, не оговоренной настоящими Правилами, право принятия решения остается за руководителями Лиги Наций.<br>
Их решение может быть обжаловано руководству ФП ФИФА или Мастер-серии (для Лиги Сайтов), которые имеют право отменить это решение и принять другое (15.1)<br>
